    private void primeroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_primeroActionPerformed
        try{
            if (rs.isLast()){
                this.ultimo.setEnabled(true);
                this.siguiente.setEnabled(true);
            }
            rs.first();
            this.primero.setEnabled(false);
            this.anterior.setEnabled(false);
            mostrarRegistro();
        } catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_primeroActionPerformed

    private void ultimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ultimoActionPerformed
       try{
            if (rs.isFirst()){
                this.primero.setEnabled(true);
                this.anterior.setEnabled(true);
            }
            rs.last();
            this.ultimo.setEnabled(false);
            this.siguiente.setEnabled(false);
            mostrarRegistro();
        } catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_ultimoActionPerformed

    private void anteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_anteriorActionPerformed
        try{
            if (rs.isLast()){
                this.ultimo.setEnabled(true);
                this.siguiente.setEnabled(true);
            }
            rs.previous();
            if (rs.isFirst()){
                this.primero.setEnabled(false);
                this.anterior.setEnabled(false);    
            }
            mostrarRegistro();
        } catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_anteriorActionPerformed

    private void siguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_siguienteActionPerformed
        try{
            if (rs.isFirst()){
                this.primero.setEnabled(true);
                this.anterior.setEnabled(true);
            }
            rs.next();
            if (rs.isLast()){
                this.ultimo.setEnabled(false);
                this.siguiente.setEnabled(false);    
            }
            mostrarRegistro();
        } catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_siguienteActionPerformed

