    public MostrarDepartamentos() {
        initComponents();
        jdbc = new AccesoJDBC();
        rs = jdbc.mosDept();
        if (rs == null){
            nRows = 0;
            this.l_posicion.setText("0 de "+nRows);
            this.primero.setEnabled(false);
            this.ultimo.setEnabled(false);
            this.anterior.setEnabled(false);
            this.siguiente.setEnabled(false);
            this.d_dept_no.setText("Sin Datos");
            this.d_dnombre.setText("Sin Datos");
            this.d_loc.setText("Sin Datos");
        } else {
            this.primero.setEnabled(false);
            this.anterior.setEnabled(false);
            try{
                ResultSetMetaData rsmd = rs.getMetaData();
                int columnas = rsmd.getColumnCount();
                JLabel[] etiquetas = new JLabel[columnas];
                etiquetas[0] = this.l_1;
                etiquetas[1] = this.l_2;
                etiquetas[2] = this.l_3;
                for (int i = 0; i < columnas; i++){
                    etiquetas[i].setText(rsmd.getColumnName(i+1));
                }
                rs.last();
                nRows = rs.getRow();
                rs.first();
                mostrarRegistro();
            } catch (Exception e){
                e.printStackTrace();
            }
        }
    }
    
    private void mostrarRegistro(){
        try{
            this.l_posicion.setText(rs.getRow()+" de "+nRows);
            this.d_dept_no.setText(rs.getString("dept_no"));
            this.d_dnombre.setText(rs.getString("dnombre"));
            this.d_loc.setText(rs.getString("loc"));
        } catch (Exception e){
            e.printStackTrace();
        }
    }
    
